package com.javaex.ex01;

public class Ex02 {
	public static void main(String[] args) {
		//실수형
		double ver01 = 3.14;
		float ver02 = 3.14f;
		System.out.println(ver01);
		System.out.println(ver02);
		
		//변수표현
		int ver11;
		ver11 =25;
		System.out.println(ver11);
		
		int ver12 =25;
		System.out.println(ver12);
		
		
		
		///////////////////////////////////
		//변수 선언
		/*
		int v01;
		int v02;
		int v03;
		*/
		
		int v01, v02, v03;
		
		v03 =25;
		v02 =24;
		v01 =23;
		
		//변수선언 + 갑대입
		//v01 = 3; --> 기존 v01값을 변경
		//int v01 = 3 --> 변수명 v01이 2개가 돼서 오류
		
		/*
		int v04 = 3;
		int v05 = 4;
		int v06 = 5;
		*/
		
		int v04 =3,v05=4, v06=5;
		System.out.println(v04);
		System.out.println(v05);
		System.out.println(v06);
		
		
		}

}
